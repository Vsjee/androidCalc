package com.example.intenttask;

public class Operations {
    public int suma(int numOne, int numTwo) {
        return numOne + numTwo;
    }

    public int resta(int numOne, int numTwo) {
        return numOne - numTwo;
    }

    public int multiplicacion(int numOne, int numTwo) {
        return numOne * numTwo;
    }

    public float division(float numOne, float numTwo) {
        return numOne / numTwo;
    }
}
